利用者が本アプリを利用するためには、当社が指定する方法により、利用者情報の登録が必要です。
当社は、利用者情報を適切に管理し、プライバシーポリシーに従って利用します。利用者情報の取扱いについては、プライバシーポリシーをご確認ください。
