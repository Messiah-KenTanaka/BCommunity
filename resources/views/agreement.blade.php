利用者は、本アプリの利用にあたり、法令や公序良俗に違反しないように行動しなければなりません。
利用者は、自身のアカウントおよびパスワードを適切に管理し、第三者に利用させないようにしなければなりません。
利用者は、本アプリ上でのコンテンツの投稿や表示に関して、著作権やプライバシーなどの権利を侵害しないように注意しなければなりません。
